x = 10
y = 5

# Arithmetic Operators
print(f"x + y = {x + y}")  # Addition: 15
print(f"x - y = {x - y}")  # Subtraction: 5
print(f"x * y = {x * y}")  # Multiplication: 50
print(f"x / y = {x / y}")  # Division: 2.0
print(f"x // y = {x // y}") # Floor Division: 2
print(f"x % y = {x % y}")  # Modulus: 0
print(f"x ** y = {x ** y}") # Exponentiation: 100000

# Comparison Operators
print(f"x == y: {x == y}")  # Equal to: False
print(f"x != y: {x != y}")  # Not equal to: True
print(f"x > y: {x > y}")    # Greater than: True
print(f"x < y: {x < y}")    # Less than: False
print(f"x >= y: {x >= y}")  # Greater than or equal to: True
print(f"x <= y: {x <= y}")  # Less than or equal to: False

# Logical Operators
a = True
b = False
print(f"a and b: {a and b}")  # Logical AND: False
print(f"a or b: {a or b}")   # Logical OR: True
print(f"not a: {not a}")     # Logical NOT: False

# Assignment Operators
x = 10
x += y  # x = x + y  => x = 15
print(f"x += y: {x}")
x -= y  # x = x - y  => x = 10
print(f"x -= y: {x}")
x *= y  # x = x * y  => x = 50
print(f"x *= y: {x}")


